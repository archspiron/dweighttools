import math
def inchToCM():
    inch = int(input('ENTER INCHES: '))
    inchresult = inch * 2.58
    inchresult = round(inchresult)
    print(inchresult)

def cmToInch():
    cm = int(input('ENTER CENTIMETERS: '))
    cmresult = cm * 0.394
    cmresult = round(cmresult)
    print(cmresult)
